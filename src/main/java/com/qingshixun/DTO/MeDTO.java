package com.qingshixun.DTO;

public class MeDTO {

	private int id;

	private String name;

	private EduDTO edu;

	private String address;

	private String gender;

	private String worktime;

	private String Email;

	private String birthday;

	private WorkstateDTO workstate;

	private int iphone;

	private String life;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public EduDTO getEdu() {
		return edu;
	}

	public void setEdu(EduDTO edu) {
		this.edu = edu;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getWorktime() {
		return worktime;
	}

	public void setWorktime(String worktime) {
		this.worktime = worktime;
	}

	public String getEmail() {
		return Email;
	}

	public void setEmail(String email) {
		Email = email;
	}

	public String getBirthday() {
		return birthday;
	}

	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}

	public WorkstateDTO getWorkstate() {
		return workstate;
	}

	public void setWorkstate(WorkstateDTO workstate) {
		this.workstate = workstate;
	}

	public int getIphone() {
		return iphone;
	}

	public void setIphone(int iphone) {
		this.iphone = iphone;
	}

	public String getLife() {
		return life;
	}

	public void setLife(String life) {
		this.life = life;
	}

}
